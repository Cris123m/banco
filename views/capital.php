            <h2>Capital</h2>
            <p>El capital se refiere a los recursos, bienes o valores que se utilizan para generar valor a través de la fabricación de otros bienes o servicios o la obtención de ganancias o utilidades sobre la tenencia o venta de valores.</p>
        </header>

        <p>El Capital es uno de los cuatro factores de producción junto con la tierra, el trabajo y la tecnología. Comprende todos los bienes durables que se utilizan para la fabricación de otros bienes o servicios. Así por ejemplo, un horno es un bien de capital para un panadero ya que lo utiliza para la fabricación de pan (otro bien) y los servicios que le entrega durarán por varios años.</p>
        <p>Para producir bienes o servicios el capital debe combinarse con otros factores productivos. La combinación precisa dependerá de la tecnología utilizada y de las características del bien o servicio producido. El capital aumenta la productividad de los otros factores productivos pero a medida que este se mantiene fino y el resto de los factores aumenta, el aumento de productividad será decreciente (ley de productividad marginal decreciente).</p>
        <p>El capital también se refiere a los recursos financieros que se invierten en un determinado proyecto para fabricación o venta de servicios, las ganancias de intereses u otras ganancias financieras.</p>


